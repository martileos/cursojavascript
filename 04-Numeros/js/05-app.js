let puntaje = 5;
console.log(puntaje);
// Imprime y luego incrementa
console.log(puntaje++);
// Aquí si imprime el incremento
console.log(puntaje);
// Incrementa y luego imprime
console.log(++puntaje);

// Imprime y luego decrementa
console.log(puntaje--);
// Aquí si imprime el decremento
console.log(puntaje);
// Decrementa y luego imprime
console.log(--puntaje);

// Abreviaturas
puntaje += 3;
console.log(puntaje);
puntaje -= 3;
console.log(puntaje);
