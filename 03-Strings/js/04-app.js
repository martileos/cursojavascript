const productos02 = "            Monitor 20 pulgadas             ";
console.log(productos02);
console.log(productos02.length);

// Eliminar espacios del inicio
console.log(productos02.trimStart());
console.log(productos02.trimStart().length);

// Eliminar espacios del final
console.log(productos02.trimEnd());
console.log(productos02.trimEnd().length);

// Eliminar todos los espacios
console.log(productos02.trimStart().trimEnd());
console.log(productos02.trimStart().trimEnd().length);
